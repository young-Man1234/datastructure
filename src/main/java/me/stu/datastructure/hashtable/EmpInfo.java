package me.stu.datastructure.hashtable;

public class EmpInfo {
    int empNum;
    int age;

    public EmpInfo(int empNum, int age) {
        this.empNum = empNum;
        this.age = age;
    }

    public int getEmpNum() {
        return empNum;
    }

    public void setEmpNum(int empNum) {
        this.empNum = empNum;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
